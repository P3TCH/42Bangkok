char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (c < 0 || c > 127)
		return ((char *)s);
	while (s[i] != 0)
		i++;
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
